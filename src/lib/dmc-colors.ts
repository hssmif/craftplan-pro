// ── sRGB → CIE LAB (D65) ──────────────────────────────────────────────
// Perceptually uniform space: equal numeric distance ≈ equal perceived
// color difference. Used for DMC matching because naive RGB distance
// produces muddy skin tones and confuses near-identical DMC variants.
function srgbToLinear(c: number): number {
  const v = c / 255;
  return v <= 0.04045 ? v / 12.92 : Math.pow((v + 0.055) / 1.055, 2.4);
}

export function rgbToLab(r: number, g: number, b: number): [number, number, number] {
  const R = srgbToLinear(r);
  const G = srgbToLinear(g);
  const B = srgbToLinear(b);
  // sRGB → XYZ (D65)
  const X = R * 0.4124564 + G * 0.3575761 + B * 0.1804375;
  const Y = R * 0.2126729 + G * 0.7151522 + B * 0.0721750;
  const Z = R * 0.0193339 + G * 0.1191920 + B * 0.9503041;
  // Normalize by D65 reference white
  const xn = X / 0.95047;
  const yn = Y / 1.0;
  const zn = Z / 1.08883;
  const f = (t: number) => (t > 0.008856 ? Math.cbrt(t) : 7.787 * t + 16 / 118);
  const fx = f(xn), fy = f(yn), fz = f(zn);
  return [116 * fy - 16, 500 * (fx - fy), 200 * (fy - fz)];
}

// Pre-computed LAB for every DMC color, cached at module load.
export const DMC_LAB: [number, number, number][] = DMC_COLORS.map(([, , hex]) => {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return rgbToLab(r, g, b);
});

// Squared CIE76 ΔE in LAB — faster than sqrt, same ordering.
export function deltaE2Lab(a: [number, number, number], b: [number, number, number]): number {
  const dl = a[0] - b[0], da = a[1] - b[1], db = a[2] - b[2];
  return dl * dl + da * da + db * db;
}

// Find the nearest DMC color to a given RGB value (LAB ΔE, perceptually accurate).
export function findNearestDMC(r: number, g: number, b: number): { dmc: string; name: string; hex: string; distance: number } {
  const lab = rgbToLab(r, g, b);
  let bestIdx = 0, bestDist = Infinity;
  for (let i = 0; i < DMC_LAB.length; i++) {
    const d = deltaE2Lab(lab, DMC_LAB[i]);
    if (d < bestDist) { bestDist = d; bestIdx = i; }
  }
  const [dmc, name, hex] = DMC_COLORS[bestIdx];
  return { dmc, name, hex, distance: Math.sqrt(bestDist) };
}

// Find nearest DMC given a precomputed LAB triple (avoids re-conversion in loops).
export function findNearestDMCFromLab(lab: [number, number, number]): { dmc: string; name: string; hex: string; idx: number } {
  let bestIdx = 0, bestDist = Infinity;
  for (let i = 0; i < DMC_LAB.length; i++) {
    const d = deltaE2Lab(lab, DMC_LAB[i]);
    if (d < bestDist) { bestDist = d; bestIdx = i; }
  }
  const [dmc, name, hex] = DMC_COLORS[bestIdx];
  return { dmc, name, hex, idx: bestIdx };
}

