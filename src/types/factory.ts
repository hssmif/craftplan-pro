// ── Factory Run ─────────────────────────────────────────────

export type FactoryRunStatus =
  | "pending"
  | "scanning"
  | "blueprinting"
  | "generating"
  | "imaging"
  | "listing"
  | "packaging"
  | "completed"
  | "ready_to_list"
  | "failed"
  | "cancelled";

export type FactoryEngine = 1 | 2 | 3 | 4 | 5;

export interface FactoryEngineLog {
  engine: FactoryEngine;
  name: string;
  status: "pending" | "running" | "done" | "error" | "skipped";
  durationMs?: number;
  error?: string;
  output?: Record<string, unknown>;
}

export interface FactoryRun {
  id: string;
  status: FactoryRunStatus;

  // Input
  keywords?: string[];
  opportunityId?: number;
  selectedListingId?: string;

  // Engine outputs
  blueprintId?: string;
  projectId?: string;
  listingImages?: string[];       // Asset IDs
  deliveryPdfAssetId?: string;

  // Tracking
  engineLog: FactoryEngineLog[];
  errorMessage?: string;
  startedAt?: string;
  completedAt?: string;
  createdAt: string;
}

