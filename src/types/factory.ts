// ── Listing Image Plan ──────────────────────────────────────

export type ListingImageKind =
  | "thumbnail"
  | "problem"
  | "dashboard"
  | "feature"
  | "method"
  | "included"
  | "delivery"
  // Family-specific narrative slots
  | "kpi-hero"           // Executive: full KPI strip hero (no device frame, data-forward)
  | "vendor-zoom"        // Editorial/Wedding: close-up on vendor or guest data
  | "itinerary-preview"  // Editorial/Travel: itinerary table showcase
  | "milestone-tracker"  // Nurture: savings goals or milestones progress
  | "bills-calendar"     // Paycheck: bills due dates and sinking funds
  | "debt-progress"      // Paycheck/Debt: debt payoff progress visualization
  | "pl-breakdown"       // Executive: P&L or revenue vs expense comparison
  | "social-proof"       // Any: trust/review/rating slide
  | "comparison"         // Any: before/after or us-vs-competitor slide
  // Dynamic sequence kinds (from VisualDirectionSpec.imageSequence)
  | "detail-zoom"        // Close-up of specific tab/feature
  | "lifestyle"          // Aspirational context shot
  | "guarantee"          // Money-back, satisfaction, trust
  // Meal planner slots
  | "meal-plan-zoom"     // Meal planner: weekly meal plan grid close-up
  | "grocery-zoom"       // Meal planner: grocery list with costs close-up
  // Student budget slots
  | "semester-zoom"      // Student: semester costs breakdown close-up
  | "subscription-audit" // Student: subscription audit table close-up
  // Debt payoff slots
  | "debt-overview"      // Debt: debt accounts with APR/payoff close-up
  | "snowball-zoom"      // Debt: month-by-month snowball tracker close-up
  | "milestones";        // Debt: payoff milestone celebrations close-up

export type CropIntent = "full" | "kpi" | "chart" | "table" | "section" | "tabs-bar";
export type MockupType = "laptop" | "tablet" | "fullscreen" | "split-layout" | "text-slide";
export type OverlayStyle = "bold-dark" | "clean-light" | "minimal-premium";

export interface ListingImageSpec {
  id: string;                 // "img_1" through "img_7"
  slot: number;               // 1-7 (Etsy listing position)
  kind: ListingImageKind;
  title: string;              // Main overlay text
  subtitle?: string;          // Secondary text
  sourceTab?: string;         // Which spreadsheet tab to capture
  sourceRange?: string;       // e.g. "A1:L23" — what cell range to show
  cropIntent?: CropIntent;    // How to frame the capture
  highlights?: string[];      // Specific elements to call out (arrows, circles)
  mockupType: MockupType;     // How the image is framed
  overlayStyle: OverlayStyle; // Text overlay aesthetic
  notes?: string;             // Implementation notes for manual capture or future automation
}

export interface ListingImagePlan {
  factoryRunId: string;
  blueprintId: string;
  productType: "sheets" | "pdf" | "excel" | "printable" | "notion";
  productName: string;
  thumbnailIndex: number;     // Which slot is the recommended thumbnail (usually 1)
  colorScheme: {              // For overlay consistency
    primary: string;
    accent: string;
    background: string;
  };
  images: ListingImageSpec[];
  captureInstructions: string; // Human-readable summary of what to capture
}

