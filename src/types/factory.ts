// ── Product Blueprint (detailed spec for GWS generation) ────

export interface BlueprintTab {
  name: string;
  purpose: string;
  columns: Array<{
    name: string;
    type: "text" | "currency" | "percent" | "date" | "formula" | "number";
    width?: number;
    formula?: string;
  }>;
  sampleRows: Array<Array<string | number | null>>;
  features: Array<"frozen_header" | "dropdown_validation" | "conditional_formatting" | "sparkline" | "chart" | "alternating_rows">;
  /**
   * Optional: route this tab to a rich, hand-crafted layout builder
   * instead of the standard column-driven data builders. When set, the
   * `columns`/`sampleRows` arrays are ignored and the layout builder
   * paints the worksheet directly from the Python reference recipe.
   */
  richLayout?: "smart-calendar" | "year-in-review" | "money-coach" | "what-if-simulator";
}

export interface BlueprintChart {
  type: "pie" | "donut" | "bar" | "column" | "line";
  title: string;
  sourceTab: string;
  sourceRange: string;        // e.g. "B2:B6" for labels
  dataRange: string;          // e.g. "D2:D6" for values
  placement: { tab: string; row: number; col: number };
  width?: number;
  height?: number;
}

export interface ProductBlueprint extends FactoryBlueprint {
  // Detailed product spec
  tabs: BlueprintTab[];
  charts: BlueprintChart[];
  colorScheme: {
    primary: string;     // hex e.g. "#1B3A5C"
    secondary: string;
    accent: string;
    background: string;
    text: string;
    success: string;
    danger: string;
  };
  sampleDataStrategy: string;
  deliveryMethod: "xlsx_download" | "sheets_link" | "both";

  // ── Gemini-First Spec Chain ──
  // These specs are generated by Gemini and drive all creative decisions.
  // Claude consumes them for deterministic building/rendering/packaging.

  /** AI-generated visual direction — controls all image/video composition */
  visualDirection?: import("./visual-direction").VisualDirectionSpec;
  /** AI-generated product concept — WHO, WHAT, WHY */
  conceptSpec?: import("./gemini-specs").ProductConceptSpec;
  /** AI-generated product structure — sections, tabs, pages */
  structureSpec?: import("./gemini-specs").ProductStructureSpec;
  /** AI-generated video creative brief */
  videoDirection?: import("./gemini-specs").VideoDirectionSpec;
  /** AI-generated listing positioning strategy */
  listingPositioning?: import("./gemini-specs").ListingPositioningSpec;
  /** AI-generated copy tone and voice direction */
  copyDirection?: import("./gemini-specs").CopyDirectionSpec;
}

