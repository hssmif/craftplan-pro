// ── Factory Run API ─────────────────────────────────────────

export type FactoryRunMode =
  | "full"                 // scan → pick best → full pipeline
  | "single_best"         // alias for full with autoPickTop=true
  | "selected_opportunity" // user picked a specific opportunity
  | "from-opportunity"    // legacy alias for selected_opportunity
  | "from-blueprint"      // skip scan+blueprint, start from existing
  | "top_n";              // build top N from a niche (batch)

export interface FactoryRunInput {
  mode: FactoryRunMode;
  keywords?: string[];
  opportunityId?: number;
  listingId?: string;
  blueprintId?: string;
  productTypeOverride?: FactoryProductType;
  autoPickTop?: boolean;
  /** For top_n mode: how many products to build */
  topN?: number;
  /** Pre-loaded opportunity data (avoids re-scanning) */
  opportunityData?: {
    title: string;
    tags?: string[];
    price?: number;
    reviews?: number;
    revenueEstimate?: number;
    niche?: string;
    description?: string;
    listingId?: string;
    imageUrls?: string[];
    ideaContext?: {
      title?: string;
      whyNow?: string;
      targetBuyer?: string;
    };
    marketContext?: {
      competition?: number | null;
      avgFavorites?: number | null;
      evidenceCount?: number;
      topTags?: string[];
    };
  };
}

