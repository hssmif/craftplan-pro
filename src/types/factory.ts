/** Everything needed to publish a product on Etsy */
export interface ReadyToListPackage {
  productAsset?: { assetId: string; downloadUrl: string; fileName: string };
  listingImages?: Array<{ slot: number; kind: string; assetId: string; downloadUrl: string }>;
  listingCopy?: {
    title: string;
    tags: string[];
    description: string;
    price: number;
    /** Anchor/original price for sale display (shown in description) */
    originalPrice?: number;
  };
  packageAsset?: { assetId: string; downloadUrl: string };
  /** URL to real-time Playwright preview of the spreadsheet dashboard */
  previewUrl?: string;
  /** URL to the generated listing video */
  videoUrl?: string;
  /** Blueprint ID for fetching previews */
  blueprintId?: string;
  /** Google Sheet ID (when built via Google Sheets API) */
  googleSheetId?: string;
  /** Direct URL to open the Google Sheet in the browser */
  googleSheetUrl?: string;
  suggestedPrice?: number;
  totalDurationMs?: number;
  /** Internal: Google Sheet ID for delivery PDF generation */
  _googleSheetId?: string;
  /** Internal: Product title for delivery PDF */
  _title?: string;
  /** Internal: Project ID for asset storage */
  _projectId?: string;
  /** Which builder produced the spreadsheet (gemini-exceljs | premium-template | legacy) */
  builderPath?: string;
  /** Gemini-generated product name (when builderPath === "gemini-exceljs") */
  geminiProductName?: string;
  /** Number of tabs in the generated spreadsheet */
  tabCount?: number;
  /** Multi-aesthetic variants — separate .xlsx files for different palettes (Etsy SKU multiplier) */
  variants?: Array<{
    key: string;
    label: string;
    fileName: string;
    fileSizeBytes: number;
    downloadUrl?: string;
    assetId?: string;
  }>;
}

// ── Etsy Publish Handoff ─────────────────────────────────────

export type EtsyPublishState = "draft_ready" | "publishing" | "draft_created" | "active" | "failed";

export interface EtsyPublishPayload {
  sourceRunId: string;
  blueprintId?: string;
  projectId?: string;

  // Listing metadata
  title: string;
  description: string;
  tags: string[];
  price: number;

  // Image assets (ordered by slot for Etsy listing photos)
  imageAssets: Array<{
    assetId: string;
    downloadUrl: string;
    slot: number;
    kind: string;
  }>;

  // The product file customers download
  digitalFileAsset?: {
    assetId: string;
    downloadUrl: string;
    fileName: string;
  };

  // ZIP bundle with instructions + listing copy
  packageAsset?: {
    assetId: string;
    downloadUrl: string;
  };

  // Etsy-specific
  taxonomyId: number;
  productType: "digital";
  categoryHint?: string;

  // Google Sheets delivery (when product is a Google Sheet)
  _googleSheetId?: string;

  // State
  state: EtsyPublishState;

  // After publish
  etsyListingId?: number;
  etsyListingUrl?: string;
}

export interface EtsyPublishResult {
  success: boolean;
  sourceRunId: string;
  etsyListingId?: number;
  etsyListingUrl?: string;
  etsyStatus: "draft" | "active";
  stepsCompleted: string[];
  errors: string[];
}

